	<div class="poetry">
    <p>The excitement builds –  all candidates have</p>
    <p>spoken;</p>
    <p>boundaries broken,</p>
    <p>the favourites tilt on</p>
    <p>unsteady stilts.</p>
    <p>All have had time on a platform-stand.</p>
    <p>Just how far, how, can you bend?</p>
    <br />
    <p>Each attacks the other-</p>
    <p>That is not the way;</p>
    <p>Let us turn round and look the other way.</p>
    <br />
    
    <p>‘Pick the weakest party,</p>
    <p>please give them a chance’;</p>
    <p>with mocking tongue the strong ones say-</p>
    <p>only to sway votes at the end of the day.</p>
    <p>From the shove and the party,</p>
    <p>you know they are feeling,</p>
    <p>despite all that one party might win.</p>
    <br />
    
    <p>Best to give your vote to</p>
    <p>the smallest of small.</p>
    <p>Down here as I watch, waiting at the polls</p>
    <p>today.</p>
    <p>Scrutineer I am called, as I watch the voting</p>
    <p>here.</p>
    <p>All the votes to be counted at the end of the day.</p>
    <p>One observes.</p>
    <br />

    <p>Old people coming at the end of their day;</p>
    <p>standing in line-</p>
    <p>even a lady of hundred and nine!</p>
    <p>Of young people, only a few</p>
    <p>dropping in slowly like the evening dew.</p>
    <p>Many young haven’t bothered to come here</p>
    <p>today;</p>
    <p>they didn’t believe in what had been said;</p>
    <p>in any way.</p>
    <br />
    
    <p>Is there some confusion</p>
    <p>with twelve parties on the ballot this bright</p>
    <p>fall day?;</p>
    <p>What does it mean to have such a choice?</p>
    <p>Only confusion which creates confusion-</p>
    <p>is that by chance, what it all</p>
    <p>really means?</p>
    <p>Some just shake their heads;</p>
    <p>others keep a straight face.</p>
    <p>Let us hope the country wins its own race.</p>
    <p>But a race to where?</p>
    <p>The end of another political game.</p>
    <p>Let’s not play games!</p>
    <p>Try making a difference,</p>
    <p>by being real and caring-</p>
    <p>That is my preference.</p>
  </div>
