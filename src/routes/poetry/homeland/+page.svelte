  <div class="poetry flex flex-col md:flex-row">
    <div>
      <pre>I have not been back </pre>
      <pre>for twenty-five years.</pre>
      <pre>I am descending slowly </pre>
      <pre>down the stairs.</pre>
      <pre>The train is leaving……</pre>
      <pre>with my friends;</pre>
      <pre>my knees are shaking……</pre>
      <pre>it never ends.</pre>
      <pre>Will they come and take me, </pre>
      <pre>I wonder-here?</pre>
      <pre>As I watch a young man </pre>
      <pre>sipping his beer.</pre>
      <pre>I start walking slowly……</pre>
      <pre>down the platform;</pre>
      <pre>waiting for that </pre>
      <pre>strange dark storm.</pre>
      <pre>But than I come to my senses, </pre>
      <pre>and think of the light which</pre>
      <pre>has been with me always-</pre>
      <pre>bright and white.</pre>
    </div>

    <div>
      <pre>If I stand up straight and </pre>
      <pre>go through that gate,</pre>
      <pre>the light will protect me, </pre>
      <pre>even if it’s late.</pre>
      <pre>To find my brother-</pre>
      <pre>(I don’t know if he lives.)</pre>
      <pre>the life that takes and gives.</pre>
      <pre>To meet my friends today or tomorrow, </pre>
      <pre>my waiting ends.</pre>
      <pre>When I found my brother, and my friends too,</pre>
      <pre>they didn’t know what to do;</pre>
      <pre>I have changed not;</pre>
      <pre>but they have a lot.</pre>
      <pre>Astonished with this find;</pre>
      <pre>all so imbedded in my mind.</pre>
      <pre>This land is no longer mine;</pre>
      <pre>now for another I hold the line.</pre>
      <pre>Canada is my Homeland.</pre>
    </div>
  </div>
</div>